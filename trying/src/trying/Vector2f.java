package trying;

public class Vector2f {
	private double x;
	private double y;
	public Vector2f(double x, double y){
		this.setX(x);
		this.setY(y);
	}
	public double getX() {
		return x;
	}
	public void setX(double x) {
		this.x = x;
	}
	public double getY() {
		return y;
	}
	public void setY(double d) {
		this.y = d;
	}
}
